package com.example.e3646.lifeblabla.object;

public class Tag {

    private String mColor;
    private String mText;

    public Tag() {

        mColor = "";
        mText = "";

    }


    public String getmColor() {
        return mColor;
    }

    public void setmColor(String mColor) {
        this.mColor = mColor;
    }

    public String getmText() {
        return mText;
    }

    public void setmText(String mText) {
        this.mText = mText;
    }

}
